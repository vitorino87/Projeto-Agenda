    /**
     * L√≥gica para montar o quadro (JInternalFrame) clicado na agenda e ao mesmo
     * tempo desmontar os outros quadros. N√£o √© poss√≠vel mostrar mais de um
     * quadro ao mesmo tempo.
     *
     * @param id
     */
    public void montarQuadroJif(int id) {
        montarJif();
        for (int i = 0; i < 4; i++) {
            if (i != id) {
                jif[i].setVisible(false);
                jif[i].dispose();
            } else {
                jif[i].setVisible(true);
                jif[i].setSize(421, 339);
                switch (i) {
                    case 0:
                        jif[i].setTitle("Inserir Contato");
                        break;
                    case 1:
                        jif[i].setTitle("Deletar Contato");
                        break;
                    case 2:
                        jif[i].setTitle("Consultar Contato");
                        break;
                    case 3:
                        jif[i].setTitle("Alterar Contato");
                        break;
                }
            }
        }
    }
