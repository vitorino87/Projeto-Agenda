    /**
     * Preenchimento dos JTextField. O id serve para diferenciar
     * entre Consulta e Altera√ß√£o
     * @param rs
     * @param id 
     * @return 
     */
    private boolean preenchendoCampos(ResultSet rs, int id) {
        if (id == 0) {
            try {
                rs.first();
                txtCelularConsultar.setText(rs.getString("celular"));
                txtEnderecoConsultar.setText(rs.getString("endereco"));
                txtIdadeConsultar.setText(rs.getString("idade"));
                txtNomeConsultar.setText(rs.getString("nome"));
                txtTelefoneConsultar.setText(rs.getString("telefone"));
                return true;
            } catch (SQLException ex) {
                System.out.println("N√£o recuperado: " + ex);
                return false;
            }
        } else {
            try {
                rs.first();
                txtCelularAlterar.setText(rs.getString("celular"));
                txtEnderecoAlterar.setText(rs.getString("endereco"));
                txtIdadeAlterar.setText(rs.getString("idade"));
                txtNomeAlterar.setText(rs.getString("nome"));
                txtTelefoneAlterar.setText(rs.getString("telefone"));
                return true;
            } catch (SQLException ex) {
                System.out.println("N√£o recuperado: " + ex);
                return false;
            }
        }

    }

